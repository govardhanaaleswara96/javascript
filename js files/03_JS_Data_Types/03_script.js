// number example
var jsVersion = 5;
console.log('value: ' + jsVersion + ' Data type: ' + typeof jsVersion);
// String Examples
var appName = 'javascript'
console.log('value: ' + appName + ' Data type: ' + typeof appName);
// boolean Examples
var jsEasy=true;
console.log('value: ' + jsEasy + ' Data type: ' + typeof jsEasy);
// reassignment example of variables
var abc;
abc = 10;
console.log('value:' + abc + ' Data type: ' + typeof abc);
abc = 'javascript';
console.log('value:' + abc + ' Data type: ' + typeof abc);
abc = true;
console.log('value:' + abc + ' Data type: ' + typeof abc);
abc = null;
console.log('value:' + abc + ' Data type: ' + typeof abc);
